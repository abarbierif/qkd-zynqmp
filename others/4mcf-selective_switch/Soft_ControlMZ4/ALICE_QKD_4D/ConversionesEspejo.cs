            //donde (Volts) corresponde a Voltaje en volts de entrada, y (dx) es la deflección en nanómetros.
            return dx;
        }
        public double namometro2VoltDM2(double nm) //   11.934313538285277830819171281268*(0.167584*y + 13.568238553081)^(1/2) - 43.960151327095665457322894787092
