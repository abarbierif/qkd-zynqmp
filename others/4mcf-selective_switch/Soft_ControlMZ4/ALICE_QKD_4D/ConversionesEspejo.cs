        public double Volt2namometroDM1(double Volts) //Curva calibración para cada actuador DM1(alice) y=0.042730*x^2+3.334440*x o x=  11.701380762930025743037678446057*(0.17092*y + 11.1184901136).^(1/2) - 39.017552071144395038614556517669;
        {

            double dx = 0.042730 * Volts * Volts + 3.334440 * Volts;

            //donde (Volts) corresponde a Voltaje en volts de entrada, y (dx) es la deflección en nanómetros.
            return dx;
        }
        public double namometro2VoltDM1(double nm) //  x=  11.701380762930025743037678446057*(0.17092*y + 11.1184901136).^(1/2) - 39.017552071144395038614556517669;
        {
            double nmp = 0.17092 * nm + 11.1184901136;

            double V = 11.701380762930025743037678446057 * Math.Pow(nmp, 0.5) - 39.017552071144395038614556517669;

            //donde (Volts) corresponde a Voltaje en volts de entrada, y (dx) es la deflección en nanómetros.
            return V;
        }
        public double Volt2namometroDM2(double Volts) //Curva calibración para cada actuador DM2(bob): y=0.041896*x^2+3.683509*x o  11.934313538285277830819171281268*(0.167584*y + 13.568238553081)^(1/2) - 43.960151327095665457322894787092
        {

