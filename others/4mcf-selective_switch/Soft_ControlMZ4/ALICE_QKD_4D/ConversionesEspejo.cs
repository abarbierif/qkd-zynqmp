        public double pi2porcentajeDM1(double n, double correcion, double plano)
        {

            double pi = 775;//Pi es de 775nm
            double nm = pi * n;
            double volt = namometro2VoltDM1(plano + nm + correcion * pi);
            double porcentaje = volt / 300 * 100;
            porcentaje = Math.Round(porcentaje * 100000) / 100000;
            return porcentaje;
        }

